#Giả sử ta muốn khảo sát tỉ lệ giới tính trong một tổng thể (population) là tất cả cư dân của một
#thành phố. Từ tổng thể, ta thu thập được một mẫu dữ liệu ngẫu nhiên (random sample) gồm 100
#người trong đó có 60 nữ và 40 nam. Dựa vào mẫu dữ liệu này, ta kiểm định nghi vấn “tỉ lệ nữ của
#thành phố là cao hơn nam” với mức ý nghĩa (significance level) 5%.